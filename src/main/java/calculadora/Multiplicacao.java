package calculadora;

/**
 * Classe responsável por realizar multiplicações entre dois valores.
 *
 * @author Nicolas Frederico
 * @since 1.0
 * @version 1.0
 */
public class Multiplicacao {
    /**
     * Método que multiplica A por B.
     *
     * @param a Primeiro inteiro.
     * @param b Segundo inteiro.
     * @return Um inteiro resultado da multiplicação entre A e B.
     */
    public int multiplicar(int a, int b) {
        return a * b;
    }
}
