public class Divisao {
    /**
     * Método que divide A por B.
     * @param a Primeiro inteiro.
     * @param b Segundo inteiro.
     * @return Um inteiro resultado da divisão entre A e B.
     */
    public int dividir(int a, int b) {
        return a / b;
    }
}
